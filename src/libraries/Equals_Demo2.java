package libraries;

public class Equals_Demo2 {

	public static void main(String[] args) {
		
//		integer type
		System.out.println(10 == 20);
		
//		char type
		System.out.println('a' == 'c');
		
//		char and double type
		System.out.println('a' == 97.0);
		
//		boolean type
		System.out.println(true == true);

	}

}
